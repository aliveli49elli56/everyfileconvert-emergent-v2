/**
 * lib/registry/library-registry.ts
 *
 * Universal Library Registry — Phase 6A
 * Single source of truth for every processing library the platform supports,
 * plans to support, or uses as a fallback.  No library code is imported here;
 * this is purely metadata.
 */

// ---------------------------------------------------------------------------
// TYPES
// ---------------------------------------------------------------------------

export type LibraryStatus =
  | "primary"       // Active default for this domain
  | "secondary"     // Active alternative
  | "fallback"      // Used only when primary/secondary unavailable
  | "premium"       // Available on paid tiers only
  | "future"        // Planned but not integrated yet
  | "experimental"; // Under evaluation / unstable

export type LibraryTier =
  | "browser"   // Runs entirely in browser (WASM / JS)
  | "server"    // Server-side only (Node.js / native binary)
  | "cloud"     // Third-party cloud API
  | "hybrid";   // Both browser and server

export type LibrarySupportedDomain =
  | "image" | "vector" | "raw" | "icon" | "3d" | "cad"
  | "video" | "audio"
  | "pdf" | "document" | "spreadsheet" | "presentation" | "ebook"
  | "archive" | "font" | "gis" | "email"
  | "code" | "webpage" | "subtitle" | "certificate"
  | "scientific" | "medical" | "disk-image" | "executable"
  | "ocr";

export interface LibraryDefinition {
  /** Unique registry identifier */
  id: string;
  /** Human-readable name */
  name: string;
  /** npm / pip / system package name */
  package: string;
  /** Official homepage or repository */
  homepage: string;
  /** SPDX license identifier */
  license: string;
  /** Whether it can run in a browser (via WASM, pure JS, etc.) */
  browserSupport: boolean;
  /** Whether a server runtime (Node, native binary) is required */
  serverRequired: boolean;
  /** Processing domains this library handles */
  supportedDomains: LibrarySupportedDomain[];
  /** Processor IDs that can use this library */
  supportedProcessors: string[];
  /** Provider IDs that are implemented on top of this library */
  supportedProviders: string[];
  /** Hosting tier */
  tier: LibraryTier;
  /** Relative priority when multiple libraries can handle the same task (lower = preferred) */
  priority: number;
  /** Integration status */
  status: LibraryStatus;
  /** Approximate uncompressed JS/WASM bundle size (bytes). 0 = server-only, unknown */
  bundleSize?: number;
  /** Input format extensions this library can read */
  inputFormats: string[];
  /** Output format extensions this library can produce */
  outputFormats: string[];
  /** True if concurrent / parallel processing is supported */
  supportsParallel: boolean;
  /** True if streaming / incremental output is possible */
  supportsStreaming: boolean;
  /** Estimated max file size this library handles reliably (bytes). 0 = unlimited/unknown */
  maxFileSizeBytes: number;
  /** Short one-line description */
  description: string;
}

