class LibraryRegistry {
  private byId: Map<string, LibraryDefinition>;
  private byDomain: Map<LibrarySupportedDomain, LibraryDefinition[]>;
  private byStatus: Map<LibraryStatus, LibraryDefinition[]>;

  constructor() {
    this.byId = new Map();
    this.byDomain = new Map();
    this.byStatus = new Map();

    for (const lib of LIBRARY_DEFINITIONS) {
      this.byId.set(lib.id, lib);

      for (const domain of lib.supportedDomains) {
        const existing = this.byDomain.get(domain) ?? [];
        existing.push(lib);
        this.byDomain.set(domain, existing);
      }

      const statusList = this.byStatus.get(lib.status) ?? [];
      statusList.push(lib);
      this.byStatus.set(lib.status, statusList);
    }
  }

  /** Get a library definition by ID */
  get(id: string): LibraryDefinition | undefined {
    return this.byId.get(id);
  }

  /** Get all libraries for a given domain, sorted by priority (lower = first) */
  forDomain(domain: LibrarySupportedDomain): LibraryDefinition[] {
    return [...(this.byDomain.get(domain) ?? [])].sort((a, b) => a.priority - b.priority);
  }

  /** Get browser-compatible libraries for a domain */
  browserLibrariesForDomain(domain: LibrarySupportedDomain): LibraryDefinition[] {
    return this.forDomain(domain).filter(l => l.browserSupport);
  }

  /** Get server-required libraries for a domain */
  serverLibrariesForDomain(domain: LibrarySupportedDomain): LibraryDefinition[] {
    return this.forDomain(domain).filter(l => l.serverRequired);
  }

  /** Get primary libraries only */
  primaryLibraries(): LibraryDefinition[] {
    return this.byStatus.get("primary") ?? [];
  }

  /** Get all libraries with a given status */
  withStatus(status: LibraryStatus): LibraryDefinition[] {
    return this.byStatus.get(status) ?? [];
  }

  /** Check if a library can convert between two formats */
  canConvert(libraryId: string, inputExt: string, outputExt: string): boolean {
    const lib = this.byId.get(libraryId);
    if (!lib) return false;
    return lib.inputFormats.includes(inputExt) && lib.outputFormats.includes(outputExt);
  }

  /** Get all library IDs that can handle a conversion */
  librariesForConversion(inputExt: string, outputExt: string): LibraryDefinition[] {
    return LIBRARY_DEFINITIONS.filter(l =>
      l.inputFormats.includes(inputExt) && l.outputFormats.includes(outputExt)
    ).sort((a, b) => a.priority - b.priority);
  }

  /** Total library count */
  get count(): number {
    return this.byId.size;
  }

  /** All library IDs */
  get ids(): string[] {
    return Array.from(this.byId.keys());
  }
}

export const libraryRegistry = new LibraryRegistry();
