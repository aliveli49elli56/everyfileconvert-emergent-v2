/**
 * lib/registry/processor-registry.ts
 * Phase 6A — Universal Processor Registry
 *
 * Single source of truth for every supported processing operation.
 * Every processor is defined as pure metadata — no library references here.
 * Business logic resolves processors through this registry only.
 */

import type { FormatCategory } from '../types/formats';
import type { ConversionOperation } from '../types/conversion';

// ---------------------------------------------------------------------------
// PROCESSOR DEFINITION INTERFACE
// ---------------------------------------------------------------------------

export type ProcessorTier = 'primary' | 'secondary' | 'fallback' | 'premium' | 'future' | 'experimental';
export type CPUProfile = 'low' | 'medium' | 'high' | 'very-high';
export type MemoryProfile = 'minimal' | 'low' | 'medium' | 'high' | 'very-high';

export interface ProcessorDefinition {
  /** Unique processor identifier (e.g. "image:convert") */
  id: ConversionOperation | string;
  /** URL-friendly slug */
  slug: string;
  /** Human-readable title */
  title: string;
  /** Description of what this processor does */
  description: string;
  /** Which format category this processor belongs to */
  category: FormatCategory;
  /** Accepted input format extensions */
  inputFormats: string[];
  /** Producible output format extensions */
  outputFormats: string[];
  /** The provider domain that handles this processor */
  providerDomain: string;
  /** Library IDs from Library Registry that can execute this processor */
  supportedLibraries: string[];
  /** Is this a premium-only operation */
  premium: boolean;
  /** Can run entirely in the browser (client-side) */
  browserSupported: boolean;
  /** Can run in a Web Worker */
  workerSupported: boolean;
  /** Supports batch file processing */
  batchSupported: boolean;
  /** Supports streaming/chunked processing */
  streamingSupported: boolean;
  /** Can leverage GPU acceleration */
  gpuSupported: boolean;
  /** Estimated peak memory usage */
  estimatedMemory: MemoryProfile;
  /** Estimated CPU load */
  estimatedCPU: CPUProfile;
  /** Is server-side execution planned in a future phase */
  futureServerSupport: boolean;
  /** SEO alias terms for long-tail search */
  seoAliases: string[];
  /** Long-tail SEO page templates */
  longTailTemplates: string[];
  /** Related converter slugs for cross-linking */
  relatedConverters: string[];
  /** Related viewer IDs */
  relatedViewers: string[];
  /** Related tool IDs */
  relatedTools: string[];
}

