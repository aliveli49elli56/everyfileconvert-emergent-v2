// PROCESSOR REGISTRY CLASS
// ---------------------------------------------------------------------------

class ProcessorRegistry {
  private processors: Map<string, ProcessorDefinition>;
  private byCategory: Map<FormatCategory, ProcessorDefinition[]>;
  private bySlug: Map<string, ProcessorDefinition>;

  constructor() {
    this.processors = new Map();
    this.byCategory = new Map();
    this.bySlug = new Map();

    for (const proc of PROCESSOR_DEFINITIONS) {
      this.processors.set(proc.id, proc);
      this.bySlug.set(proc.slug, proc);

      const list = this.byCategory.get(proc.category) || [];
      list.push(proc);
      this.byCategory.set(proc.category, list);
    }
  }

  /** Get processor by id */
  get(id: string): ProcessorDefinition | undefined {
    return this.processors.get(id);
  }

  /** Get processor by slug */
  getBySlug(slug: string): ProcessorDefinition | undefined {
    return this.bySlug.get(slug);
  }

  /** Get all processors */
  getAll(): ProcessorDefinition[] {
    return [...PROCESSOR_DEFINITIONS];
  }

  /** Get processors by category */
  getByCategory(category: FormatCategory): ProcessorDefinition[] {
    return this.byCategory.get(category) || [];
  }

  /** Get processors that support a given input format */
  getForInputFormat(ext: string): ProcessorDefinition[] {
    const e = ext.toLowerCase();
    return PROCESSOR_DEFINITIONS.filter(p => p.inputFormats.includes(e) || p.inputFormats.includes('*'));
  }

  /** Get processors that support a given output format */
  getForOutputFormat(ext: string): ProcessorDefinition[] {
    const e = ext.toLowerCase();
    return PROCESSOR_DEFINITIONS.filter(p => p.outputFormats.includes(e) || p.outputFormats.includes('*'));
  }

  /** Get processors that support a conversion pair */
  getForConversion(inputExt: string, outputExt: string): ProcessorDefinition[] {
    return this.getForInputFormat(inputExt).filter(p =>
      p.outputFormats.includes(outputExt.toLowerCase()) || p.outputFormats.includes('*')
    );
  }

  /** Get browser-supported processors */
  getBrowserSupported(): ProcessorDefinition[] {
    return PROCESSOR_DEFINITIONS.filter(p => p.browserSupported);
  }

  /** Get free processors */
  getFree(): ProcessorDefinition[] {
    return PROCESSOR_DEFINITIONS.filter(p => !p.premium);
  }

  /** Get worker-supported processors */
  getWorkerSupported(): ProcessorDefinition[] {
    return PROCESSOR_DEFINITIONS.filter(p => p.workerSupported);
  }

  /** Get total count */
  count(): number {
    return PROCESSOR_DEFINITIONS.length;
  }

  /** Get processors by library */
  getByLibrary(libraryId: string): ProcessorDefinition[] {
    return PROCESSOR_DEFINITIONS.filter(p => p.supportedLibraries.includes(libraryId));
  }
}

export const processorRegistry = new ProcessorRegistry();
